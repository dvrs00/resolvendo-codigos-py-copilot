# Vamos receber dois dados diferentes do usuário e concatena-los em uma única string?!

# Solicita o primeiro dado ao usuário
dado1 = input("Por favor, insira o primeiro dado: ")

# Solicita o segundo dado ao usuário
dado2 = input("Por favor, insira o segundo dado: ")

# Concatena os dois dados
string_concatenada = dado1 + dado2

# Exibe a string concatenada
print("Os dados concatenados são:", string_concatenada)
